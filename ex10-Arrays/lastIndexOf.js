// lastIndexOf() - Retorna o índice referente à última ocorrência de um valor em um array, varrendo o array de trás para frente

const arrayNumbers = [1,2,3,4,1,2,3,4]

const posicaoNumber = arrayNumbers.indexOf(4); //3
const posicao = arrayNumbers.lastIndexOf(4); //7

console.log(posicaoNumber);
console.log(posicao);